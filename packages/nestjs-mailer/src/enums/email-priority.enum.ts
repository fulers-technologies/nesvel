/**
 * Email Priority Enum
 *
 * Priority levels for email sending
 *
 * @enum EmailPriority
 */
export enum EmailPriority {
  /**
   * High priority - urgent/transactional emails
   */
  HIGH = 'high',

  /**
   * Normal priority - standard emails
   */
  NORMAL = 'normal',

  /**
   * Low priority - marketing/promotional emails
   */
  LOW = 'low',
}
